// Normaliza string para comparación: lowercase + sin tildes
function normalizeStr(s: string): string {
  return (s || '')
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '');
}

// Detecta si una firma del JSON aparece en el título/resumen del artículo.
// Prioriza firmas largas (apellido + nombre) sobre cortas.
const PERIODISTAS_SORTED_BY_LENGTH = [...PERIODISTAS].sort(
  (a, b) => b.firma.length - a.firma.length
);

export function detectJournalist(
  title: string,
  summary: string,
  mediaName: string
): Periodista | null {
  const haystackText = normalizeStr(`${title} ${summary}`);
  const mediaNorm = normalizeStr(mediaName);

  for (const p of PERIODISTAS_SORTED_BY_LENGTH) {
    const firmaNorm = normalizeStr(p.firma);
    if (!firmaNorm || firmaNorm.length < 6) continue; // muy corta, riesgo de falso positivo
    if (haystackText.includes(firmaNorm)) {
      // Verificar que el medio principal o secundario tenga afinidad con la fuente
      // del artículo (si la conocemos), pero como hay solapamientos (CFK escribe en Cenital
      // y P/12, etc.), aceptamos cualquier coincidencia de firma.
      return p;
    }
  }

  // Si no encontramos firma pero el medio es uno conocido del JSON, devolvemos null.
  // (el caller puede tratar 'medio conocido sin firma' distinto a 'medio desconocido')
  return null;
}

// Score por orientación para el boost. Más alto = más prioridad.
export function orientationScore(o: OrientacionPeriodista | undefined): number {
  switch (o) {
    case 'peronista': return 3;
    case 'progresista': return 3;
    case 'independiente_critico': return 2.5;
    case 'conservador': return 1; // menor prioridad pero entran
    default: return 1.5; // sin firma identificada
  }
}
